public class Variables {
    static void main() {

        // create // declare
        int age;
        //assign value
        age = 20;
        System.out.println(age);

        // define or initialize
        int totalMarks = 80;
        System.out.println(totalMarks);

        // case sensitive
        int weight = 45;
        int WEIGHT = 40;
        System.out.println("weight " + weight);
        System.out.println("WEIGHT " + WEIGHT);

        // Starting Letter
        int marks = 10;
        int MARKS = 11;
        int _marks = 12;
        int $marks = 13;

        //Subsequent Letters
        int height5 = 21;
        int height_love = 33;
        int height$12Love = 44;
        int height$ = 23;

        // No Reserved Keyword
//        int class = 25;

        // length
        int helloJeeKaiseHoSaareMyNameIsLoveBabbar = 101;
        int a = 1;
        int b = 2;
        int c = 3;

        // CamelCase
        int myNameIsLoveBabbar = 51;
        int totalAmount = 100;

        //Constant
        int DAYS_IN_YEAR = 365;
    }
}
